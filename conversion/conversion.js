function boolToString(arg) {

    if (!arg || arg == undefined || typeof (arg) != "boolean") {
        console.log("AFTC.js: Conversion.js: boolToString(str): Error - input is not a boolean!");
        return "error";
    }

    if (arg) {
        return "true";
    } else {
        return "false";
    }
}
